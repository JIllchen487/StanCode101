import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    cur = l1
    num1 = ''
    while cur is not None:
        num1 += str(cur.val)
        cur = cur.next
    num1 = int(num1)

    cur = l2
    num2 = ''
    while cur is not None:
        num2 += str(cur.val)
        cur = cur.next
    num2 = int(num2)

    num3 = num1+num2
    num3 = str(num3)

    prior = ListNode(num3[0], None)
    if len(num3) > 1:
        for char in num3[1:]:
            cur = ListNode(char, prior)
            prior = cur
    else:
        cur = prior

    #######################
    return cur


